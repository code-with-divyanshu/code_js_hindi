//while loop

// while (condition) {} // syntax

let i = 0;
while (i <= 10) {
  console.log(`Value of index is: ${i}`);
  i++;
}

let myArr = ["Flash", "Batman", "Superman"];
let j = 0;
while (j < myArr.length) {
  console.log(myArr[j]);
  j++;
}

// do-while loop

// do {} while (condition); //syntax

let score = 11;

do {
  console.log(`Score is: ${score}`);
  score++;
} while (score <= 10); // run atleast one time

